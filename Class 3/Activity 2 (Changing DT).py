#Typecasting 
name = 'Aayush'
age = 14
height = 5.5
isstudent = True
print('My name is' ,name)
print('The data type of name is', type(name))
print('\nMy age is' ,age)
print('The data type of age is', type(age))
print('\nMy height is' ,height)
print('The data type of height is' ,type(height))
print('\nI am a student' ,isstudent)
print('The data type of isstudent is' ,type(isstudent))

#Changing the Data Type
age=str (age)
print('\nNow the data type of age is:' ,type(age))
height=str (height)
print('Now the data type of height is:' ,type(height))
isstudent=str (isstudent)
print('Now the data type of isstudent is:' ,type(isstudent))